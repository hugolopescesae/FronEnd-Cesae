package ex_03;

public class Retângulo {

    // Atributos
    private double largura;
    private double altura;

    // Constructor
    public Retângulo(double largura, double altura) {
        this.largura = largura;
        this.altura = altura;
    }

    // Getters
    public double getLargura() {
        return largura;
    }

    public double getAltura() {
        return altura;
    }
}
